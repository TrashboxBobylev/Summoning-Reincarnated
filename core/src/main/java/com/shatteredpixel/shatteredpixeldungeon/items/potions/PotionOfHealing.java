public class PotionOfHealing extends Potion {

	{
		icon = ItemSpriteSheet.Icons.POTION_HEALING;

		bones = true;
	}
	
	@Override
	public void apply( Hero hero ) {
		identify();
		cure( hero );
		heal( hero );
	}

	@Override
	public int gooInfuseUses() {
		return 2;
	}

	@Override
	public void gooMinionAttack(Char ch) {
		for (int dir: PathFinder.NEIGHBOURS9){
			if (Random.Int(2) == 0){
				Char chOnDir = Actor.findChar(ch.pos + dir);
				if (chOnDir != null && chOnDir.alignment == Char.Alignment.ALLY) {
					Buff.affect(chOnDir, Healing.class).setHeal(ch.HT/3*4, 0.2f, 0);
				} else {
					GameScene.add( Blob.seed( ch.pos + dir, 10, Regrowth.class ) );
				}
			}
		}
	}

	public static void heal(Char ch ){
		if (ch == Dungeon.hero && Dungeon.isChallenged(Challenges.NO_HEALING)){
			pharmacophobiaProc(Dungeon.hero);
		} else {
			//starts out healing 30 hp, equalizes with hero health total at level 11
			Healing healing = Buff.affect(ch, Healing.class);
			healing.setHeal((int) (0.8f * ch.HT + 14), 0.25f, 0);
			healing.applyVialEffect();
			if (ch == Dungeon.hero){
				GLog.p( Messages.get(PotionOfHealing.class, "heal") );
			}
		}
	}

	public static void pharmacophobiaProc( Hero hero ){
		// harms the hero for ~40% of their max HP in poison
		Buff.affect( hero, Poison.class).set(4 + hero.lvl/2);
	}

	@Override
	public void shatter( int cell ) {

		if (Dungeon.level.heroFOV[cell]) {
			setKnown();

			splash( cell );
			Sample.INSTANCE.play( Assets.Sounds.SHATTER );
		}

		GameScene.add( Blob.seed( cell, 450, HealGas.class ) );
		Char entity;

		if ((entity = Actor.findChar(cell)) != null && entity.alignment == Char.Alignment.ALLY && Dungeon.hero.hasTalent(Talent.ENERGIZED_SUPPORT)){
			for (Staff.Charger charger : Dungeon.hero.buffs(Staff.Charger.class)){
				charger.gainCharge((2 + Dungeon.hero.pointsInTalent(Talent.ENERGIZED_SUPPORT))*50f / (float) charger.staff().getChargeTurns());
			}
			Item.updateQuickslot();
		}
	}
	
	public static void cure( Char ch ) {
		Buff.detach( ch, Poison.class );
		Buff.detach( ch, Cripple.class );
		Buff.detach( ch, Weakness.class );
		Buff.detach( ch, Vulnerable.class );
		Buff.detach( ch, Bleeding.class );
		Buff.detach( ch, Blindness.class );
		Buff.detach( ch, Drowsy.class );
		Buff.detach( ch, Slow.class );
		Buff.detach( ch, Vertigo.class);
	}

	@Override
	public int value() {
		return isKnown() ? 30 * quantity : super.value();
	}
}
